class CodeConverter
{
private:
	iconv_t cd;
public:
	// 构造
	CodeConverter(const char *from_charset,const char *to_charset) 
	{
		cd = iconv_open(to_charset,from_charset);
	}
	
	// 析构
	~CodeConverter()
	{
		iconv_close(cd);
	}
	
	// 转换输出
	int convert(char *inbuf,int inlen,char *outbuf,int outlen)
	{
		char **pin = &inbuf;
		char **pout = &outbuf;
		
		memset(outbuf,0,outlen);
//		printf("@@@@@@@@@@@@@ inlen=%d,outlen=%d\n",inlen,outlen);
		//printf("begin to iconv pin=%p, pout=%d, ilen=%d,outlen=%d\n",*pin,*pout,inlen,outlen);
		int ret = iconv(cd,pin,(size_t *)&inlen,pout,(size_t *)&outlen);
		int errtimes = 0;
		if(ret == -1)
		//while(errtimes<100)
		{
			printf("iconv error! errno=%d, src=%s, desc=%s\n",errno,inbuf,outbuf);
			//printf("iconv error! pin=%p, pout=%d, ilen=%d,outlen=%d\n",*pin,*pout,inlen,outlen);
			//ret = iconv(cd,pin,(size_t *)&inlen,pout,(size_t *)&outlen);
			errtimes++;
		}
//		printf("@@@@@@@@@@@@@ inlen=%d,outlen2=%d,ret=%d\n",inlen,outlen,ret);
		return ret;
	}
};
