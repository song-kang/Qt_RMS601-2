	// 作    者:  邵凯田
	// 创建时间:  2011-11-14 15:34
	// 参数说明:  @y/M/d/h/m/s表示年月日时分秒
	// 返 回 值:  SOC
	//////////////////////////////////////////////////////////////////////////
	static inline int makeSOC(int y,int M,int d,int h,int m,int s)
	{
		struct tm t;
		memset(&t,0,sizeof(t));
		t.tm_year  = y-1900;
		t.tm_mon   = M-1;//月要减1
		t.tm_mday  = d;
		t.tm_hour  = h;
		t.tm_min   = m;
		t.tm_sec   = s;
		return (int)mktime(&t);   
	}

